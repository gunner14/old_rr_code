-- mapping number to baseweight
function GetBaseWeight(x)
  return 10.0 * (1.0 + 1.0 / (math.exp(-100 * (x - 0.019)) + 1.0))
end


-- mapping viewcnt to weight
function GetViewCntWeight(x)
  return 10.0 * (0.5 + 1.0 / (math.exp(-100 * (x - 0.019)) + 1.0))
end

-- Get ViewCnt Decay
function GetViewCntDecay(mystype, myvc)
  mydecay = 0
  myvc = myvc + 1

  if myvc <= 2 then
    if GenderStypeViewCntWeight[mystype] then
      mydecay = GenderStypeViewCntWeight[mystype][myvc]
    else
      pos = string.find(mystype, ":")
      if pos then
        mystype = string.sub(mystype, 0, pos - 1)
      end

      if viewstypeDecay[mystype] then
        mydecay = viewstypeDecay[mystype][myvc]
      else
        mydecay = defaultDecay[myvc]
      end
    end
  end

  if myvc > 2 then
    mydecay = 0.005
  end

  return mydecay
end


