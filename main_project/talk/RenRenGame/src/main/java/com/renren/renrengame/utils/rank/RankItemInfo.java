package com.renren.renrengame.utils.rank;

public class RankItemInfo {
    
	public String getAppLogo() {
		return appLogo;
	}
	public void setAppLogo(String appLogo) {
		this.appLogo = appLogo;
	}
	public String getAppIcon() {
		return appIcon;
	}
	public void setAppIcon(String appIcon) {
		this.appIcon = appIcon;
	}
	public String getAppLink() {
		return appLink;
	}
	public void setAppLink(String appLink) {
		this.appLink = appLink;
	}
	public String getAppName() {
		return appName;
	}
	public void setAppName(String appName) {
		this.appName = appName;
	}
	public double getRating() {
		return rating;
	}
	public void setRating(float rating) {
		this.rating = rating;
	}
	public String getCate() {
		return cate;
	}
	public void setCate(String cate) {
		this.cate = cate;
	}
	public int getUpOrDown() {
		return upOrDown;
	}
	public void setUpOrDown(int upOrDown) {
		this.upOrDown = upOrDown;
	}
	public String getComment_account() {
		return comment_account;
	}
	public void setComment_account(String comment_account) {
		this.comment_account = comment_account;
	}
	
	public String appLogo;
	public String appIcon;
	public String appLink;
	public String appName;
	public double rating;
	public String cate;
	public int upOrDown;
	public String comment_account;
	
}
